package thumbnail

import (
	"encoding/base64"
)

// noopService is a no-op implementation for E2E testing
type noopService struct{}

// NewNoopService creates a new noop thumbnail service
func NewNoopService() Service {
	return &noopService{}
}

// Generate returns a mock thumbnail for E2E tests
func (s *noopService) Generate(req GenerateRequest) (*GenerateResponse, error) {
	/* Return a 1x1 transparent PNG as base64 for E2E tests */
	mockPNG := []byte{
		0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
		0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
		0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4,
		0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41,
		0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
		0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00,
		0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE,
		0x42, 0x60, 0x82,
	}

	thumbnailBase64 := base64.StdEncoding.EncodeToString(mockPNG)

	return &GenerateResponse{
		URL:       req.URL,
		Thumbnail: thumbnailBase64,
		Size:      req.Size,
	}, nil
}
